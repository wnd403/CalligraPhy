package com.kcjsoft.calligraphy;

public class CommunityItem {

    String title;
    String name;
    String content;
    String hour;
    int iv;

    public CommunityItem(String title, String name, String content, String hour, int iv) {
        this.title = title;
        this.name = name;
        this.content = content;
        this.hour = hour;
        this.iv = iv;
    }
}
