package com.kcjsoft.calligraphy;

public class OnedayItem {

    String title;
    String name;
    String address;
    String hour;

    int iv;

    public OnedayItem(String title, String name, String context, String hour, int iv) {
        this.title = title;
        this.name = name;
        this.address = context;
        this.hour = hour;
        this.iv = iv;
    }
}
