package com.kcjsoft.calligraphy;

public class BusinessItem {

    String name;
    String phone;
    String email;
    String youtube;
    String career;

    int iv;

    public BusinessItem(String name, String phone, String email, String youtube, String career, int iv) {
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.youtube = youtube;
        this.career = career;
        this.iv = iv;
    }
}
